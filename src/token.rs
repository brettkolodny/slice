#[derive(PartialEq, Clone, Debug)]
pub enum Token {
    Illegal,
    EOF,
    Identity(String),
    Int(isize),
    Str(String),
    True,
    False,
    Assign,
    Plus,
    Minus,
    Divide,
    Comma,
    SemiColon,
    Colon,
    LParen,
    RParen,
    LBrace,
    RBrace,
    LBracket,
    RBracket,
    Function,
    Let,
    End,
    NewLine,
    StringType,
    BoolType,
    IntType,
    Output,
}
